const saveVideoMetadata = async (req, res) => {
  try {
    const {
      problemId,
      cloudinaryPublicId,
      secureUrl,
      duration,
    } = req.body;

    const userId = req.result._id;

    // Verify the upload with Cloudinary
    //send all the data if it is present
    const cloudinaryResource = await cloudinary.api.resource(
      cloudinaryPublicId,
      { resource_type: 'video' }
    );
    
    if (!cloudinaryResource) {
      return res.status(400).json({ error: 'Video not found on Cloudinary' });
    }

    // Check if video already exists for this problem and user
    const existingVideo = await SolutionVideo.findOne({
      problemId,
      userId,
      cloudinaryPublicId
    });

    if (existingVideo) {
      return res.status(409).json({ error: 'Video already exists' });
    }

    // const thumbnailUrl = cloudinary.url(cloudinaryResource.public_id, {
    // resource_type: 'image',  
    // transformation: [
    // { width: 400, height: 225, crop: 'fill' },
    // { quality: 'auto' },
    // { start_offset: 'auto' }  
    // ],
    // format: 'jpg'
    // });
    const thumbnailUrl = cloudinary.image("cloudinaryResource.public_id",{resource_type:"video"});

    // Create video solution record
    const videoSolution = await SolutionVideo.create({
      problemId,
      userId,
      cloudinaryPublicId,
      secureUrl,
      duration: cloudinaryResource.duration || duration,
      thumbnailUrl
    });




    res.status(201).json({
      message: 'Video solution saved successfully',
      videoSolution: {
        id: videoSolution._id,
        thumbnailUrl: videoSolution.thumbnailUrl,
        duration: videoSolution.duration,
        uploadedAt: videoSolution.createdAt
      }
    });

  } catch (error) {
    console.error('Error saving video metadata:', error);
    res.status(500).json({ error: 'Failed to save video metadata' });
  }
};

const deleteVideo = async (req, res) => {
  try {
    const { problemId } = req.params;
    const userId = req.result._id;

    // First check if video exists
    const video = await SolutionVideo.findOne({ problemId });
    
    if (!video) {
      return res.status(404).json({ 
        error: 'Video Solution Not Found',
        message: `No video solution exists for problem ID: ${problemId}`,
        problemId: problemId
      });
    }

    // Delete from database
    await SolutionVideo.findOneAndDelete({ problemId });
    
    // Delete from Cloudinary
    await cloudinary.uploader.destroy(video.cloudinaryPublicId, { 
      resource_type: 'video', 
      invalidate: true 
    });

    res.json({ 
      success: true,
      message: 'Video solution deleted successfully',
      deletedVideo: {
        problemId: video.problemId,
        videoId: video._id
      }
    });

  } catch (error) {
    console.error('Error deleting video:', error);
    res.status(500).json({ 
      error: 'Failed to delete video solution',
      message: error.message 
    });
  }
};

